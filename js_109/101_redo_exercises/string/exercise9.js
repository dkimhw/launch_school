/*

Problem:

Input
- A word (string)
- text (string)

Output:
- Integer
- The integer is the # of times that word appears in the text
- Is a word separated by a whitespace? Yes

Example:

const text = 'Sed ut perspiciatis unde omnis iste natus error sit voluptatem accusantium doloremque laudantium, totam rem aperiam, eaque ipsa quae ab illo inventore veritatis et quasi architecto beatae vitae dicta sunt explicabo. Nemo enim ipsam voluptatem quia voluptas sit aspernatur aut odit aut fugit, sed quia consequuntur magni dolores eos qui ratione voluptatem sequi nesciunt. Neque porro quisquam est, qui dolorem ipsum quia dolor sit amet, consectetur, adipisci velit, sed quia non numquam eius modi tempora incidunt ut labore et dolore magnam aliquam quaerat voluptatem. Ut enim ad minima veniam, quis nostrum exercitationem ullam corporis suscipit laboriosam, nisi ut aliquid ex ea commodi consequatur? Quis autem vel eum iure reprehenderit qui in ea voluptate velit esse quam nihil molestiae consequatur, vel illum qui dolorem eum fugiat quo voluptas nulla pariatur?';

searchWord('sed', text);      // 3

Algorithm:

Step 1: Create an array of words from the input text
  Also create a counter to track how many times the word appears in txt

Step 2: Loop over that array starting at 0 index

Step 3: For each index, check if the input word is the same if so increase the counter

Step 4: Return counter

*/

let searchWord = (word, text) => {
  let wordArr = text.toLowerCase().split(' ');
  let wordCount = 0;

  wordArr.forEach(wd => {
    if (wd === word.toLowerCase()) {
      wordCount += 1;
    }
  });

  return wordCount;
};

